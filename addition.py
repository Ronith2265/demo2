# Function to add two numbers
def add_numbers(a, b):
    return a + b

# Input from user
num1 = float(input("Enter first number: "))
num2 = float(input("Enter second number: "))

# Compute sum
sum_result = add_numbers(num1, num2)

# Display result
print(f"The sum of {num1} and {num2} is {sum_result}")
